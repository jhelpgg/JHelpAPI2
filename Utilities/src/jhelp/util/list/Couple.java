package jhelp.util.list;

/**
 * Couple of two ints
 */
public class Couple
{
    /**First int*/
    public final int first;
    /**Second int*/
    public final int second;

    /**
     * Create the couple
     * @param first First int
     * @param second Second int
     */
    public Couple(final int first, final int second)
    {
        this.first = first;
        this.second = second;
    }
}
